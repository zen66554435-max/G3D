# دالة لطباعة العربي بشكل صحيح
print_ar() {
    printf "%s\n" "$1"
}

show_banner() {
    clear
    echo ""
    echo -e "${C}   ██████╗  ██████╗ ██████╗ ${NC}"
    echo -e "${C}  ██╔════╝ ██╔═══██╗╚════██╗${NC}"
    echo -e "${C}  ██║  ███╗██║   ██║ █████╔╝${NC}"
    echo -e "${C}  ██║   ██║██║   ██║ ╚═══██╗${NC}"
    echo -e "${C}  ╚██████╔╝╚██████╔╝██████╔╝${NC}"
    echo -e "${C}   ╚═════╝  ╚═════╝ ╚═════╝ ${NC}"
    echo ""

    # استخدام printf لطباعة العربي
    printf "${P}  ╔═══════════════════════════════════════╗\n${NC}"
    printf "${P}  ║  ${Y}%-35s${P}  ║\n${NC}" "G3D - معلومات البريد والرقم"
    printf "${P}  ║  ${G}%-35s${P}  ║\n${NC}" "المطور: الجنرال"
    printf "${P}  ╚═══════════════════════════════════════╝\n${NC}"
    echo ""
}
